package com.example.minesweeper;

public class Cord {

    public float xCord;

    private float yCord;


    public Cord(float x, float y) {
        xCord = x;
        yCord = y;
    }




    public float getxCord() {
        return xCord;
    }

    public float getyCord() {
        return  yCord;
    }



}
